import XCTest
import BitByteData

class LsbBitReaderBenchmarks: XCTestCase {

    func testAdvance() {
        self.measure {
            let reader = LsbBitReader(data: Data(count: 10_485_760)) // 10 MB

            for _ in 0..<5_000_000 * 8 {
                reader.advance()
            }
        }
    }

    func testAdvanceRealistic() {
        self.measure {
            let reader = LsbBitReader(data: Data(count: 10_485_760)) // 10 MB

            for _ in 0..<9_300_000 {
                reader.advance(by: 6)
                reader.advance(by: 3)
            }
        }
    }

